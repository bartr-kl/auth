'use client'

import Link from 'next/link'
import { usePathname } from 'next/navigation'
import { useAuth, useProfile } from '@/hooks'

const navigation = [
  { name: 'Dashboard', href: '/dashboard', icon: HomeIcon },
  { name: 'Users', href: '/dashboard/users', icon: UsersIcon, requiresStaff: true },
  { name: 'Courts', href: '/dashboard/courts', icon: CourtsIcon, requiresAdmin: true },
  { name: 'Settings', href: '/dashboard/settings', icon: SettingsIcon },
]

export function Sidebar() {
  const pathname = usePathname()
  const { canManageUsers, isAdmin } = useAuth()
  const { fullName, initials } = useProfile()

  const filteredNav = navigation.filter(item => {
    if (item.requiresAdmin) return isAdmin
    if (item.requiresStaff) return canManageUsers
    return true
  })

  return (
    <div className="flex h-full w-64 flex-col bg-gray-900">
      {/* Logo */}
      <div className="flex h-16 shrink-0 items-center px-6">
        <span className="text-xl font-bold text-white">PaddleStack</span>
      </div>

      {/* Navigation */}
      <nav className="flex flex-1 flex-col px-4 py-4">
        <ul className="flex flex-1 flex-col gap-1">
          {filteredNav.map((item) => {
            const isActive = pathname === item.href || pathname.startsWith(item.href + '/')
            return (
              <li key={item.name}>
                <Link
                  href={item.href}
                  className={`group flex items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium transition-colors ${
                    isActive
                      ? 'bg-gray-800 text-white'
                      : 'text-gray-400 hover:bg-gray-800 hover:text-white'
                  }`}
                >
                  <item.icon className="h-5 w-5 shrink-0" />
                  {item.name}
                </Link>
              </li>
            )
          })}
        </ul>

        {/* User info at bottom */}
        <div className="mt-auto border-t border-gray-800 pt-4">
          <div className="flex items-center gap-3 px-3 py-2">
            <div className="flex h-9 w-9 items-center justify-center rounded-full bg-gray-700 text-sm font-medium text-white">
              {initials || '?'}
            </div>
            <div className="flex-1 min-w-0">
              <p className="truncate text-sm font-medium text-white">
                {fullName || 'Loading...'}
              </p>
            </div>
          </div>
        </div>
      </nav>
    </div>
  )
}

